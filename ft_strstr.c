#include "libft.h"

char	*ft_strstr(const char *haystack, const char *needle)
{
	int		i;
	int		j;

	i = 0;
	if (needle[i] == '\0')
		return ((char *)&haystack[0]);
	while (haystack[i] != '\0')
	{
		j = 0;
		if (haystack[i] == needle[j])
		{
			while (needle[j] == haystack[i + j])
				j++;
			if (needle[j] == '\0' || haystack[j] == '\0')
				return ((char *)&haystack[i]);
		}
		i++;
	}
	return (0);
}
